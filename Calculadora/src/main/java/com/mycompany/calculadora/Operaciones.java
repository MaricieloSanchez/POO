public class Operaciones {
    public String suma(int n1, int n2){
        return String.valueOf(n1+n2);
    }
    public String resta(int n1, int n2){
        return String.valueOf(n1-n2);
    }
    public String multiplicacion(int n1, int n2){
        return String.valueOf(n1*n2);
    }
    public String division(int n1, int n2){
        return String.valueOf(n1/n2);
    }
}
